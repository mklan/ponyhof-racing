package de.ponyhofgang.chat3000;

public class ChatStrings {

	private String stringLeft;
	private String stringRight;
	
	
	public ChatStrings(String stringLeft, String stringRight){
		
		this.stringLeft = stringLeft;
		this.stringRight = stringRight;
		
	}
	
	
	public String getStringRight() {
		return stringRight;
	}
	
	public String getStringLeft() {
		return stringLeft;
	}
	
	
	
	
	
}
